    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        
        try {
            Productos pTmp = this.pTmp;
            
            double precio = Double.parseDouble(jTextFieldPrecio.getText().trim());
            int stock = Integer.parseInt(jSpinnerStock.getValue().toString().trim());

            String ivaString = jComboBoxIva.getSelectedItem().toString().trim();
            double iva = Double.parseDouble(ivaString);

            String descripcion = jTextFieldDescripcion.getText().trim();

            Tipoproducto tpTmp = new Tipoproducto();
            if (jComboBoxTipoProducto.getSelectedItem().toString().trim().equalsIgnoreCase("comida")) {
                tpTmp = tpjc.findTipoproducto(1);
            } else if (jComboBoxTipoProducto.getSelectedItem().toString().trim().equalsIgnoreCase("bebida")) {
                tpTmp = tpjc.findTipoproducto(2);
            } else if (jComboBoxTipoProducto.getSelectedItem().toString().trim().equalsIgnoreCase("postre")) {
                tpTmp = tpjc.findTipoproducto(3);
            }

            pTmp.setPrecio(BigDecimal.valueOf(precio));
            pTmp.setStock(stock);
            pTmp.setIva(BigDecimal.valueOf(iva));
            pTmp.setDescripcion(descripcion);
            pTmp.setIdTipoProducto(tpTmp);
            
            pjc.edit(pTmp);
            
            JOptionPane.showMessageDialog(null, "¬°Producto editado correctamente!");
            AdminCrud.cargarDatosJTable();
            dispose();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

